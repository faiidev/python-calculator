angka_pertama = int(input("Masukkan Angka Pertama : "))
angka_kedua = int(input("Masukkan Angka Kedua   : "))

print("=====================")

hasil_penjumlahan = angka_pertama + angka_kedua
hasil_pengurangan = angka_pertama - angka_kedua
hasil_perkalian = angka_pertama * angka_kedua
hasil_pembagian = angka_pertama / angka_kedua
hasil_sisa_bagi = angka_pertama % angka_kedua
hasil_pangkat = angka_pertama ** angka_kedua

print("Hasil Penjumlahan : ", hasil_penjumlahan)
print("Hasil Pengurangan : ", hasil_pengurangan)
print("Hasil Perkalian   : ", hasil_perkalian)
print("Hasil Pembagian   : ", hasil_pembagian)
print("Hasil Sisa Bagi   : ", hasil_sisa_bagi)
print("Hasil Pangkat     : ", hasil_pangkat)   
